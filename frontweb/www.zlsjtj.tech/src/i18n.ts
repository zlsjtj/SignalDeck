export function useI18n() {
  const language = useAppStore((s) => s.language);
  const t = (key: I18nKey) => messages[language][key] ?? key;
  return { language, t };
}

export function byLang(zh: string, en: string) {
  return useAppStore.getState().language === 'en' ? en : zh;
}
